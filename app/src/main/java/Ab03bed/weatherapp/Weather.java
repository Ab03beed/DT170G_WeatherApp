package Ab03bed.weatherapp;

public class Weather {
    public String air_temperature, probability_of_precipitation, wind_speed, cloud_area_fraction;

    public String getAir_temperature() {
        return air_temperature;
    }

    public void setAir_temperature(String air_temperature) {
        this.air_temperature = air_temperature;
    }

    public String getProbability_of_precipitation() {
        return probability_of_precipitation;
    }

    public void setProbability_of_precipitation(String probability_of_precipitation) {
        this.probability_of_precipitation = probability_of_precipitation;
    }

    public String getWind_speed() {
        return wind_speed;
    }

    public void setWind_speed(String wind_speed) {
        this.wind_speed = wind_speed;
    }

    public String getCloud_area_fraction() {
        return cloud_area_fraction;
    }

    public void setCloud_area_fraction(String cloud_area_fraction) {
        this.cloud_area_fraction = cloud_area_fraction;
    }




}
